int **copyarray(int map[24][24])
{
	int **array;
	array = (int **)malloc(sizeof(int *) * 24);
	for (int i = 0; i < 24; i++)
	{
		array[i] = (int *)malloc(sizeof(int) * 24);
	}
	for (int i = 0; i < 24; i++)
	{
		for (int j = 0; j < 24; j++)
		{
			array[i][j] = map[i][j];
		}
	}
	return (array);
}

void free_array(int **array)
{
	for (int i = 0; i < 24; i++)
	{
		free(array[i]);
	}
	free(array);
}


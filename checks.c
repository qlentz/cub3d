#include "cub3d.h"

int	end_cub(char *s)
{
	int	len;

	len = ft_strlen(s);
	if (s[len - 1] != 'b' || s[len - 2] != 'u'
		|| s[len - 3] != 'c' || s[len - 4] != '.')
		return (0);
	return (1);
}
